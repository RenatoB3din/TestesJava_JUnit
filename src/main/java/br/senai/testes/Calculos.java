package br.senai.testes;

public class Calculos {

    public int Somar(int num1, int num2){
        int resultado = 0;
        resultado = num1 + num2;
        return resultado;
    }

    public int Subtrair(int num1, int num2){
        int resultado = 0;
        resultado = num1 - num2;
        return resultado;
    }

    public int Multiplicar(int num1, int num2) {
        int resultado = 0;
        resultado = num1 * num2;
        return resultado;
    }

    public int Dividir(int num1, int num2) {
        int resultado = 0;
        resultado = num1/num2;
        return resultado;
    }
}
